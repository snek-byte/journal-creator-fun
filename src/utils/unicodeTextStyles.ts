export const transformText = (text: string, style: keyof typeof textStyles): string => {
  if (!style || !textStyles[style]) return text;
  
  return text.split('').map(char => {
    return textStyles[style].chars[char] || char;
  }).join('');
};

export const getTextStyles = () => {
  return Object.entries(textStyles).map(([key, value]) => ({
    value: key,
    label: value.name
  }));
};

export type TextStyle = keyof typeof textStyles;
