// Utility function to transform text based on selected style
export function applyTextStyle(text: string, style: TextStyle): string {
  if (!text) return '';
  if (!style || style === 'normal') return text;

  // Handle special styles that don't use unicode mapping
  switch (style) {
    case 'strikethrough':
      return text.split('').join('Ì¶') + 'Ì¶';
    case 'underline':
      return text.split('').join('Ì²') + 'Ì²';
    case 'upside-down':
      // Fix: Don't reverse the text array, instead map each character to its upside-down version
      const flippedChars: Record<string, string> = {
        'a': 'É', 'b': 'q', 'c': 'É”', 'd': 'p', 'e': 'Ç', 'f': 'ÉŸ', 'g': 'Æƒ', 'h': 'É¥',
        'i': 'á´‰', 'j': 'É¾', 'k': 'Ê', 'l': 'l', 'm': 'É¯', 'n': 'u', 'o': 'o', 'p': 'd',
        'q': 'b', 'r': 'É¹', 's': 's', 't': 'Ê‡', 'u': 'n', 'v': 'ÊŒ', 'w': 'Ê', 'x': 'x',
        'y': 'Ê', 'z': 'z', '.': 'Ë™', '[': ']', '(': ')', '{': '}', '?': 'Â¿', '!': 'Â¡',
        '"': 'â€', "'": ',', 'â€¿': 'â€', 'â…': 'â†', 'âˆ´': 'âˆµ', '&': 'â…‹', '_': 'â€¾',
        '\\': '/', '<': '>', ';': '', ' ': ' '
      };
      // Reverse the characters correctly
      return text.split('').map(char => 
        flippedChars[char.toLowerCase()] || char
      ).reverse().join('');
  }

  // Use unicode mapping for other styles
  if (unicodeMap[style]) {
    return text.split('').map(char => unicodeMap[style][char] || char).join('');
  }

  // Default to returning the original text if style not found
  return text;
}

