/**
 * Apply a specific text style to a string
 * @param text The input text
 * @param style The style to apply
 * @returns The styled text
 */
export const applyTextStyle = (text: string, style: TextStyle | string): string => {
  // Convert style parameter to TextStyle type or use a default
  const styleKey = style as TextStyle;
  
  // For CSS-based styles, just return the original text
  if (['highlight', 'shadow', 'glow', 'outline', 'blur', 'reveal'].includes(styleKey)) {
    return text;
  }

  // If style doesn't exist, return the original text
  if (!characterMaps[styleKey]) {
    return text;
  }

  // Apply character mapping
  const charMap = characterMaps[styleKey];
  return text.split('').map(char => charMap[char] || char).join('');
};

/**
 * Get a list of all available text styles
 * @returns Array of TextStyle values
 */
export const getAvailableTextStyles = (): TextStyle[] => {
  return Object.keys(characterMaps) as TextStyle[];
};
