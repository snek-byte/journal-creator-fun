// Text style transformation utility function
export const applyTextStyle = (text: string, style: TextStyle): string => {
  if (!text) return text;
  
  const styleMap = characterMaps[style];
  if (!styleMap) return text;
  
  return text.split('').map(char => styleMap[char] || char).join('');
};

// Function to get all available text styles
export const getAvailableTextStyles = (): TextStyle[] => {
  return Object.keys(characterMaps) as TextStyle[];
};
